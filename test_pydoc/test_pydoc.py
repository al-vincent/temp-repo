def sum_ints(param1, param2):
    """A sample function sum two integers.

    Args:
        param1 (int): an integer value
        param2 (int): an integer value
    
    Returns: 
        int, sum of param1 and param2
    """
    return param1 + param2

def multiply_ints(param1, param2):
    """A sample function to multiply two integers.

    Args:
        param1 (int]): an integer value
        param2 (int): an integer value
    
    Returns:
        int, the product of param1 and param2
    """
    return param1 * param2